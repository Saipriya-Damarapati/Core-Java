package com.cglearning.tutorial_1;

/** Java file can have any number of classes; 
  * but only one public class; whose name is same as java file name
  */
public class HelloWorldProgram {

	// main method is the starting point of the program
	
	/**
	 * public - to call this method from outside the class
	 * static - we do not need to create object to run this method; it can run by itself
	 * void - it does not return anything
	 * @param args - command line arguments that are passed as strings
	 */
	public static void main(String[] args) {
		System.out.println("Hello World...!");
	}

}
