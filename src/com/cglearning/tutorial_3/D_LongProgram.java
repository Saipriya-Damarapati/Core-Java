public class D_LongProgram {

	public static void main(String[] args) {
		System.out.println("long data type is used when int is not sufficient enough to hold the value");
		System.out.println("long data type can hold value from -9,223,372,036,854,775,808 to -9,223,372,036,854,775,807");
		System.out.println("Default Size: 8 bytes");
		System.out.println("Default value: 0");
		
		long i = 2147483649L;
		System.out.println("Maximum value int data type can store is " + i);
		System.out.println("byte, short, int, long are the data types used to store whole numbers");
	}

}
